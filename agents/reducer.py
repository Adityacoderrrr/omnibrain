import logging
from .state import AgentState

logger = logging.getLogger("omnibrain.agents.reducer")


def reducer(state: AgentState) -> AgentState:
    """
    Reducer node that merges, formats, and finalizes outputs from the specialist agents.

    Args:
        state (AgentState): The current agent execution state.

    Returns:
        AgentState: The finalized agent state.
    """
    logger.info("Reducer merging and finalizing specialist responses.")
    try:
        # Validate that response is present
        if not state.get("response"):
            logger.warning("No response generated by specialists. Setting standard message.")
            state["response"] = "I don't have enough information to answer this question."

        # Ensure citations and retrieved collections are valid lists
        state["citations"] = state.get("citations") or []
        state["retrieved_docs"] = state.get("retrieved_docs") or []
        state["retrieved_images"] = state.get("retrieved_images") or []

        # Deduplicate citations if any overlap exists
        unique_citations = []
        seen = set()
        for cit in state["citations"]:
            if isinstance(cit, dict):
                key = (cit.get("page"), cit.get("source_type"), cit.get("snippet"))
                if key not in seen:
                    seen.add(key)
                    unique_citations.append(cit)
        state["citations"] = unique_citations

        # Append final trace message
        state["agent_trace"] = ["Reducer: Consolidated final response and citations"]
        logger.info("Reducer execution complete.")

    except Exception as exc:
        logger.exception("Error in reducer node: %s", exc)
        state["agent_trace"] = [f"Reducer error: {str(exc)}"]

    return state